package DOMORE;
// wap to print natural number from 1 to 10
public class p2 {

	public static void main(String[] args) {
		
        System.out.println("1 to 10 natural numbers");
        System.out.println();
        for(int a=1;a<=10;a++)
        {
        	System.out.println(a);
        }
        System.out.println("-------------------------------");
        
        System.out.println("11 to 20 odd number");
        System.out.println();
        for(int j=11;j<=20;j++)
        {
        	if(j%2==1)
        	{
        		System.out.println(j);
        	}
        }
       
	}

}
