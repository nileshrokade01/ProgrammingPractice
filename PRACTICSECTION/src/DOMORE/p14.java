package DOMORE;
//WAP TO SWAP TO NO BY USING 3RD VARIABLE
public class p14 {

	public static void main(String[] args) 
	{
		int a=3;
		int b=7;
		System.out.println("BEFORE SWAPPING");
		System.out.println("a:"+a);
		System.out.println("b:"+b);
		
		System.out.println("AFTER SWAPPING");
		int third=a;
		a=b;
		b=third;
		System.out.println("a:"+a);
		System.out.println("b:"+b);
		

	}

}
